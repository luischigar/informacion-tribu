package com.example.demo.entityDto;


public class CelulaDto {
	
	private String nombreCelula;
	
	private String nombreEquipo;
	
	private String nombreProducto;
	
	private Long idTribu;

	public String getNombreCelula() {
		return nombreCelula;
	}

	public void setNombreCelula(String nombreCelula) {
		this.nombreCelula = nombreCelula;
	}

	public String getNombreEquipo() {
		return nombreEquipo;
	}

	public void setNombreEquipo(String nombreEquipo) {
		this.nombreEquipo = nombreEquipo;
	}

	public String getNombreProducto() {
		return nombreProducto;
	}

	public void setNombreProducto(String nombreProducto) {
		this.nombreProducto = nombreProducto;
	}

	public Long getIdTribu() {
		return idTribu;
	}

	public void setIdTribu(Long idTribu) {
		this.idTribu = idTribu;
	}

}
